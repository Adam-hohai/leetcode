/**
 * Definition for singly-linked list.
 * public class ListNode {
 *     int val;
 *     ListNode next;
 *     ListNode(int x) { val = x; }
 * }
 */
class Solution {
    public int kthToLast(ListNode head, int k) {
        ListNode cur1 = head, cur2 = head;
        for (int i = 1; i < k; ++i){
            cur2 = cur2.next;
        }
        while (cur2.next != null){
            cur1 = cur1.next;
            cur2 = cur2.next;
        }
        return cur1.val;
    }
}
